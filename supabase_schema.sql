create or replace function public.audit_row_changes()
returns trigger
language plpgsql
security definer
set search_path = public
as $$
declare
  action_name text;
  row_id text;
  row_sede text;
begin
  action_name := case tg_op
    when 'INSERT' then 'CREAR'
    when 'UPDATE' then 'MODIFICAR'
    when 'DELETE' then 'ELIMINAR'
  end;

  row_id := coalesce((to_jsonb(new)->>'id'), (to_jsonb(old)->>'id'));
  row_sede := coalesce((to_jsonb(new)->>'sede_id'), (to_jsonb(old)->>'sede_id'));

  insert into public.auditoria (
    accion,
    entidad,
    entidad_id,
    usuario_id,
    valor_anterior,
    valor_nuevo,
    sede_id
  )
  values (
    action_name,
    tg_table_name,
    row_id,
    auth.uid(),
    case when tg_op in ('UPDATE', 'DELETE') then to_jsonb(old) else null end,
    case when tg_op in ('INSERT', 'UPDATE') then to_jsonb(new) else null end,
    row_sede
  );

  return coalesce(new, old);
end;
$$;

