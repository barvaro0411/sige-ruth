create or replace function public.current_profile_role()
returns text
language sql
stable
security definer
set search_path = public
as $$
  select p.rol::text
  from public.perfiles p
  where p.id = auth.uid()
    and p.activo = true
  limit 1
$$;

create or replace function public.current_profile_sede()
returns text
language sql
stable
security definer
set search_path = public
as $$
  select p.sede_id::text
  from public.perfiles p
  where p.id = auth.uid()
    and p.activo = true
  limit 1
$$;

create or replace function public.has_role(allowed_roles text[])
returns boolean
language sql
stable
security definer
set search_path = public
as $$
  select coalesce(public.current_profile_role() = any(allowed_roles), false)
$$;

create or replace function public.can_access_sede(target_sede text)
returns boolean
language sql
stable
security definer
set search_path = public
as $$
  select coalesce(
    public.has_role(array['director', 'jefe_utp'])
    or public.current_profile_sede() = target_sede,
    false
  )
$$;

